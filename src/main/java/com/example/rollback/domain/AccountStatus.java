package com.example.rollback.domain;

/**
 * 계좌 상태 열거형
 * 
 * <p>계좌의 현재 상태를 정의합니다. 상태에 따라 계좌에서 수행할 수 있는
 * 거래의 종류가 제한됩니다.</p>
 */
public enum AccountStatus {
    /**
     * 활성 상태
     * <p>모든 입출금 거래가 가능한 정상 상태입니다.</p>
     */
    ACTIVE,
    
    /**
     * 동결 상태
     * <p>보안상의 이유나 법적 요청으로 인해 모든 거래가 일시적으로 중단된 상태입니다.</p>
     */
    FROZEN,
    
    /**
     * 폐쇄 상태
     * <p>계좌가 완전히 종료된 상태로, 더 이상 어떠한 거래도 수행할 수 없습니다.</p>
     */
    CLOSED,
    
    /**
     * 정지 상태
     * <p>관리자에 의해 일시적으로 거래가 제한된 상태입니다. 해제 후 재사용 가능합니다.</p>
     */
    SUSPENDED
}