package com.example.rollback.domain;

/**
 * 계좌 유형 열거형
 * 
 * <p>은행에서 제공하는 다양한 계좌 유형을 정의합니다. 각 유형에 따라
 * 거래 한도, 수수료 정책, 이자율 등이 다르게 적용됩니다.</p>
 */
public enum AccountType {
    /**
     * 입출금 자유로운 보통예금 계좌
     * <p>일상적인 거래에 사용되며, 입출금이 자유롭고 수수료가 저렴합니다.</p>
     */
    CHECKING,
    
    /**
     * 저축성 예금 계좌
     * <p>장기적인 자금 목표를 위한 계좌로, 높은 이자율을 제공하지만 출금에 제한이 있을 수 있습니다.</p>
     */
    SAVINGS,
    
    /**
     * 신용 계좌
     * <p>월초계좌 한도가 제공되며, 신용 등급에 따라 한도가 결정됩니다.</p>
     */
    CREDIT,
    
    /**
     * 법인 계좌
     * <p>기업과 법인을 위한 전문 계좌로, 대규모 거래와 특별한 금융 서비스를 지원합니다.</p>
     */
    BUSINESS
}