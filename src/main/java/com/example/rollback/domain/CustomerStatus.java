package com.example.rollback.domain;

/**
 * 고객 상태 열거형
 * 
 * <p>고객의 현재 상태를 정의합니다. 상태에 따라 고객이 이용할 수 있는
 * 은행 서비스의 종류가 제한됩니다.</p>
 */
public enum CustomerStatus {
    /**
     * 활성 상태
     * <p>모든 은행 서비스를 정상적으로 이용할 수 있는 상태입니다.</p>
     */
    ACTIVE,
    
    /**
     * 비활성 상태
     * <p>장기 미사용 등으로 일부 서비스가 제한된 상태입니다. 재활성화 가능합니다.</p>
     */
    INACTIVE,
    
    /**
     * 정지 상태
     * <p>보안상의 이유나 규제 위반으로 서비스 이용이 일시적으로 중단된 상태입니다.</p>
     */
    SUSPENDED,
    
    /**
     * 폐쇄 상태
     * <p>고객과의 계약이 종료된 상태로, 더 이상 서비스를 이용할 수 없습니다.</p>
     */
    CLOSED
}