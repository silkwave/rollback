package com.example.rollback.domain;

/**
 * 고객 유형 열거형
 * 
 * <p>은행 고객의 종류를 구분합니다. 고객 유형에 따라
 * 제공되는 서비스, 수수료 정책, 리스크 평가 기준 등이 달라집니다.</p>
 */
public enum CustomerType {
    /**
     * 개인 고객
     * <p>일반 개인 고객으로, 개인 예금, 대출, 카드 등 개인 금융 서비스를 이용합니다.</p>
     */
    INDIVIDUAL,
    
    /**
     * 법인 고객
     * <p>기업, 법인, 단체 등으로, 법인 계좌, 기업 대출, 무역 금융 등 비즈니스 금융 서비스를 이용합니다.</p>
     */
    BUSINESS
}