package com.example.rollback.domain;

/**
 * 리스크 레벨 열거형
 * 
 * <p>고객의 리스크 수준을 정의합니다. 리스크 레벨은 거래 모니터링,
 * 한도 설정, 추가 인증 요구 등에 사용됩니다.</p>
 */
public enum RiskLevel {
    /**
     * 저위험
     * <p>정상적인 거래 패턴을 보이는 고객으로, 기본적인 서비스를 제공합니다.</p>
     */
    LOW,
    
    /**
     * 중위험
     * <p>일부 거래에 대해 추가 모니터링이 필요한 고객입니다.</p>
     */
    MEDIUM,
    
    /**
     * 고위험
     * <p>심층적인 모니터링과 거래 제한이 필요한 고객입니다.</p>
     */
    HIGH
}