package com.example.rollback.domain;

/**
 * 거래 상태 열거형
 * 
 * <p>거래의 현재 처리 상태를 정의합니다. 거래가 진행됨에 따라
 * 상태가 변경되며, 각 상태는 거래의 성공/실패 여부를 나타냅니다.</p>
 */
public enum TransactionStatus {
    /**
     * 대기 상태
     * <p>거래가 생성되었지만 아직 처리되지 않은 상태입니다. 검증 및 승인을 기다립니다.</p>
     */
    PENDING,
    
    /**
     * 완료 상태
     * <p>거래가 성공적으로 처리된 상태입니다. 자금 이동이 완료되었습니다.</p>
     */
    COMPLETED,
    
    /**
     * 실패 상태
     * <p>거래 처리 중 오류가 발생하여 실패한 상태입니다. 실패 사유가 기록됩니다.</p>
     */
    FAILED,
    
    /**
     * 취소 상태
     * <p>사용자 또는 시스템에 의해 거래가 취소된 상태입니다.</p>
     */
    CANCELLED,
    
    /**
     * 롤백/환불 상태
     * <p>이전에 완료된 거래가 롤백되거나 환불된 상태입니다.</p>
     */
    REVERSED
}