package com.example.rollback.domain;

/**
 * 거래 유형 열거형
 * 
 * <p>은행에서 처리되는 모든 종류의 거래 유형을 정의합니다.
 * 거래 유형에 따라 처리 방식, 수수료 정책, 회계 처리 등이 달라집니다.</p>
 */
public enum TransactionType {
    /**
     * 입금
     * <p>계좌로 자금이 입금되는 거래입니다. 이체, 직접 입금 등이 포함됩니다.</p>
     */
    DEPOSIT,
    
    /**
     * 출금
     * <p>계좌에서 자금이 출금되는 거래입니다. 현금 인출, 이체, 결제 등이 포함됩니다.</p>
     */
    WITHDRAWAL,

    /**
     * 수수료
     * <p>거래 수수료, 월 수수료, 기타 서비스 수수료를 처리하는 거래입니다.</p>
     */
    FEE,
    
    /**
     * 이자
     * <p>예금 이자, 대출 이자 등 이자 지급/차감 거래입니다.</p>
     */
    INTEREST,
    
    /**
     * 벌금/위약금
     * <p>약정 위반, 연체 등으로 인한 벌금이나 위약금을 처리하는 거래입니다.</p>
     */
    PENALTY
}